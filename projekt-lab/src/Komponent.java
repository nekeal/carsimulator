public class Komponent {
    private String nazwa;
    private float waga;
    private float cena;

    public Komponent(String nazwa, float waga, float cena) {
        this.nazwa = nazwa;
        this.waga = waga;
        this.cena = cena;
    }

    public String getNazwa(){
        return " ";
    }

    public float getCena() {
        return cena;
    }

    public float getWaga(){
        return waga;
    }
}
